	public function before($event = array()) {
		$db = ConnectionManager::getDataSource($this->connection);
		$db->cacheSources = FALSE;
		return TRUE;
	}

	public function after($event = array()) {
		if(isset($event['create'])) {
            switch($event['create']) {
				case 'pages':
					$this->insertPages();
					break;
				case 'posts_categories':
					$this->insertPosts();
					break;
                case 'users_groups':
                    $this->insertUsers();
                    break;
            }
        }
	}
	
	public function insertPages() {
		$pages = ClassRegistry::init('MeCms.Page');
		$pages->create();
		
		$save = $pages->saveAll(array(
			'title'	=> 'Your first page',
			'slug'	=> 'your-first-page',
			'text'	=> 'Hello, this is your first page!'			
		), array('counterCache' => FALSE, 'deep' => TRUE, 'validate' => FALSE));
	}
	
	public function insertPosts() {
		$categories = ClassRegistry::init('MeCms.PostsCategory');
		$categories->create();
		
		$save = $categories->saveAll(array(
			array(
				'title'			=> 'Your first category', 
				'slug'			=> 'your-first-category',
				'description'	=> 'This is your first category',
				'post_count'	=> '1',
				'Post' => array(array(
					'user_id'	=> '1', 
					'title'		=> 'Your first post', 
					'slug'		=> 'your-first-post', 
					'text'		=> 'Hello, this is your first post!'
				))
			)
		), array('counterCache' => FALSE, 'deep' => TRUE, 'validate' => FALSE));
	}
	
	public function insertUsers() {
		$groups = ClassRegistry::init('MeCms.UsersGroup');
